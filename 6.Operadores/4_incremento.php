<?php

print '<h3>POSTINCREMENTO</h3>';
$a = 5;
print $a++.'<br>';
print $a;

print '<h3>PREINCREMENTO</h3>';
$a = 5;
print ++$a.'<br>';
print $a;

print '<h3>POSTDECREMENTO</h3>';
$a = 5;
print $a--.'<br>';
print $a;

print '<h3>PREDECREMENTO</h3>';
$a = 5;
print --$a.'<br>';
print $a;